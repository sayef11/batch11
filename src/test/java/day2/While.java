package day2;

public class While {

    public static void main(String[] args) {

        //while loop starts

        String[] states = new String[6];
        states[0]= "New York";
        states[1]= "Arizona";
        states[2]= "Chicago";
        states[3]= "California";
        states[4]="Kentucky";
        states[5]="Michigan";

        int i=0;
        while (i <states.length){
            System.out.println("My state is "+ states[i]);
            i++;

        }//end of the while loop








    }//main method ends



}//class ends
