package day3;

public class MultipleCondition {


    public static void main(String[] args) {

        int A,B,C;

        A=20;
        B=20;
        C=50;

        if (A+B>C){

            System.out.println("it's greater than C");
        } //if ends
        else if (A+B<C){
            System.out.println("It's less than C");
        } //else if ends
        else {
            System.out.println("It's equal to C");
        }// else statement ends

    }//main method ends

}//class ends
